#Creating a Dictionary
my_dict= {
     "Name": "ABC",
     "Age": 25,
     "City": "Pune"
}
print("Original Dictionary:", my_dict)

#Accessing Elements
print("Name:", my_dict["Name"])

#Adding New Key-Value Pair
my_dict["Country"]="India"
print("After adding country:", my_dict)

#Updating Value
my_dict["Age"]=26
print("After updating age:", my_dict)